package Aula3;

public class ifELse {
    public static void main(String[] args) {
        byte n1 = 5, n2 = 10;

        if (n1>n2){
            System.out.println("O numero 1 é maior do que o numero 2.");
        }else if(n1<n2){
            System.out.println("O numero 1 é menor do que o numero 2");
        }else {
            System.out.println("Os numeros sao iguais");
        }
    }
}
